export const renderColor = (group) => {
    if(group == "BROWN_2") {
        return "#7b3500";
    } else if(group == "BROWN") {
        return "#7b5400";
    } else if(group == "BLUE") {
        return "#00add9";
    } else if(group == "ORANGE") {
        return "#ed5904";
    } else if(group == "YELLOW") {
        return "yellow";
    } else if(group == "GREEN") {
        return "#09ff00";
    } else if(group == "PINK") {
        return "pink";
    } else {
        return "#047100";
    }
}